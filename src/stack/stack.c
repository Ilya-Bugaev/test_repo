#include "stack.h"

// Инициализация стека
void init_stack(Stack* s) {
    s->top = -1;  // -1 означает, что стек пуст
}

// Добавление элемента в стек
void push(Stack* s, char c) {
    if (s->top < MAX_SIZE - 1) {  // Проверяем, не полон ли стек
        s->top++;                 // Увеличиваем указатель вершины
        s->data[s->top] = c;      // Сохраняем элемент
    }
}

// Извлечение элемента из стека
char pop(Stack* s) {
    if (s->top >= 0) {           // Проверяем, не пуст ли стек
        char c = s->data[s->top]; // Берем элемент с вершины
        s->top--;                 // Уменьшаем указатель вершины
        return c;                 // Возвращаем элемент
    }
    return '\0';  // Если стек пуст, возвращаем нулевой символ
}

// Просмотр вершины стека без извлечения
char peek(Stack* s) {
    if (s->top >= 0) {           // Проверяем, не пуст ли стек
        return s->data[s->top];  // Возвращаем элемент с вершины
    }
    return '\0';  // Если стек пуст, возвращаем нулевой символ
}

// Проверка на пустоту стека
int is_empty(Stack* s) {
    return s->top == -1;  // Если top = -1, стек пуст
}

// Проверка на переполнение стека
int is_full(Stack* s) {
    return s->top == MAX_SIZE - 1;  // Если top достиг максимума
} 
